// Subclass (// class Cat "implements" the Animal interface)
class Cat implements Animal {
    public void animalSound() {
        // The body of animalSound() is provided here
        System.out.println("The cat sounds: miyav");
    }
    public void sleep() {
        // The body of sleep() is provided here
        System.out.println("The cat sleeps: zzzzzz");
    }
}
